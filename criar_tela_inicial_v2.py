    "    def _lista_recentes(self, parent, limite=5):\n",
    "        arqs = sorted(DIR_CROQUIS.glob('*.sicro'), reverse=True)[:limite]\n",
    "        if not arqs:\n",
    "            tk.Label(parent,\n",
    "                     text='Nenhum croqui salvo ainda.',\n",
    "                     font=FONTE_SMALL, bg=FUNDO_BASE,\n",
    "                     fg=TEXTO_TERCIARIO).pack(padx=28, anchor='w')\n",
    "            return\n",
    "        self._arqs_recentes = arqs\n",
    "        for i, arq in enumerate(arqs):\n",
    "            self._item_recente(parent, arq, i)\n",
    "\n",
    "    def _item_recente(self, parent, arq, idx):\n",
    "        try:\n",
    "            with open(arq) as f: d = json.load(f)\n",
    "        except Exception:\n",
    "            d = {}\n",
    "        bo    = d.get('bo', '—')\n",
    "        local = d.get('local', arq.stem)[:45]\n",
    "        data  = d.get('data', '')\n",
    "        modo  = d.get('modo', 'zero')\n",
    "        tag_cor = AZUL_MEDIO if modo=='zero' else COR_SUCESSO\n",
    "        tag_txt = 'Manual' if modo=='zero' else 'Drone'\n",
    "        row = tk.Frame(parent, bg=FUNDO_CARD, cursor='hand2')\n",
    "        row.pack(fill='x', padx=28, pady=2)\n",
