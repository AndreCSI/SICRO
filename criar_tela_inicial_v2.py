    "        tk.Frame(row, bg=AZUL_BORDA, width=3).pack(side='left', fill='y')\n",
    "        info = tk.Frame(row, bg=FUNDO_CARD)\n",
    "        info.pack(side='left', fill='both', expand=True, padx=12, pady=8)\n",
    "        top_row = tk.Frame(info, bg=FUNDO_CARD)\n",
    "        top_row.pack(fill='x')\n",
    "        tk.Label(top_row, text=f'BO {bo}',\n",
    "                 font=FONTE_BODY_BOLD, bg=FUNDO_CARD,\n",
    "                 fg=TEXTO_PRIMARIO).pack(side='left')\n",
    "        tk.Label(top_row, text=f'  {tag_txt}',\n",
    "                 font=FONTE_MICRO, bg=tag_cor,\n",
    "                 fg=TEXTO_PRIMARIO, padx=6).pack(side='left', padx=8)\n",
    "        tk.Label(top_row, text=data, font=FONTE_MICRO,\n",
    "                 bg=FUNDO_CARD, fg=TEXTO_TERCIARIO).pack(side='right')\n",
    "        tk.Label(info, text=local, font=FONTE_SMALL,\n",
    "                 bg=FUNDO_CARD, fg=TEXTO_SECUNDARIO, anchor='w').pack(fill='x')\n",
    "        def _hin(e, r=row):\n",
    "            r.config(bg=FUNDO_HOVER)\n",
    "            for w in r.winfo_children(): _bg(w, FUNDO_HOVER)\n",
    "        def _hout(e, r=row):\n",
    "            r.config(bg=FUNDO_CARD)\n",
    "            for w in r.winfo_children(): _bg(w, FUNDO_CARD)\n",
