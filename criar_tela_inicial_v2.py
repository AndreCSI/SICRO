    "            for ch in w.winfo_children(): _bg(ch, c)\n",
    "        def _abrir(e, a=arq): self.on_abrir(a)\n",
    "        row.bind('<Enter>', _hin); row.bind('<Leave>', _hout)\n",
    "        row.bind('<Double-Button-1>', _abrir)\n",
