    "        tk.Label(corpo, text='Departamento de Criminalistica - DC',\n",
    "                 font=('Segoe UI', 9),\n",
    "                 bg=AZUL_ESCURO, fg=CINZA_CLARO).pack()\n",
    "        tk.Label(corpo,\n",
    "                 text='Desenvolvido por Andre Ricardo Barroso - Perito Criminal',\n",
    "                 font=('Segoe UI', 9, 'italic'),\n",
    "                 bg=AZUL_ESCURO, fg=CINZA_MEDIO).pack(pady=(3, 12))\n",
    "\n",
    "        sty = ttk.Style()\n",
    "        sty.configure('Sp.Horizontal.TProgressbar',\n",
    "                       troughcolor=AZUL_ESCURO, background=AMARELO,\n",
    "                       borderwidth=0, thickness=6)\n",
    "        self.barra = ttk.Progressbar(corpo, length=380,\n",
    "                                     style='Sp.Horizontal.TProgressbar',\n",
    "                                     maximum=100, value=0)\n",
