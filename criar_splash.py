    ")\n",
    "try:\n",
    "    from PIL import Image, ImageTk\n",
    "    PIL_OK = True\n",
    "except ImportError:\n",
    "    PIL_OK = False\n",
    "\n",
    "\n",
    "class SplashScreen(tk.Toplevel):\n",
    "    \"\"\"\n",
    "    Tela de splash exibida na inicializacao do SICRO PCI/AP.\n",
    "    Mostra brasao, nome do sistema e barra de progresso animada.\n",
    "    Fecha automaticamente apos completar a animacao.\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, parent, img_brasao=None):\n",
    "        super().__init__(parent)\n",
    "        self.overrideredirect(True)\n",
    "        w, h = 560, 420\n",
    "        sw, sh = self.winfo_screenwidth(), self.winfo_screenheight()\n",
    "        self.geometry(f'{w}x{h}+{(sw-w)//2}+{(sh-h)//2}')\n",
    "        self.configure(bg=AZUL_ESCURO)\n",
    "        self.lift()\n",
    "        self.attributes('-topmost', True)\n",
    "\n",
    "        tk.Frame(self, bg=AMARELO, height=6).pack(fill='x')\n",
    "        corpo = tk.Frame(self, bg=AZUL_ESCURO)\n",
    "        corpo.pack(fill='both', expand=True, padx=48, pady=16)\n",
    "\n",
    "        if img_brasao and PIL_OK:\n",
    "            img = img_brasao.resize((110, 110), Image.LANCZOS)\n",
    "            self._tk = ImageTk.PhotoImage(img)\n",
    "            tk.Label(corpo, image=self._tk, bg=AZUL_ESCURO).pack(pady=(4, 0))\n",
    "        else:\n",
    "            tk.Label(corpo, text='Brasao', font=('Segoe UI', 52),\n",
    "                     bg=AZUL_ESCURO, fg=AMARELO).pack(pady=(4, 0))\n",
