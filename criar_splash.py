    "        tk.Label(corpo, text='POLICIA CIENTIFICA DO AMAPA',\n",
    "                 font=('Segoe UI', 10, 'bold'),\n",
    "                 bg=AZUL_ESCURO, fg=AMARELO).pack(pady=(4, 0))\n",
    "        tk.Label(corpo, text='SICRO PCI/AP',\n",
    "                 font=('Segoe UI', 26, 'bold'),\n",
    "                 bg=AZUL_ESCURO, fg=BRANCO).pack(pady=(6, 0))\n",
    "        tk.Label(corpo, text='Sistema de Croquis Periciais de Transito',\n",
    "                 font=('Segoe UI', 10),\n",
    "                 bg=AZUL_ESCURO, fg=CINZA_CLARO).pack(pady=(2, 0))\n",
