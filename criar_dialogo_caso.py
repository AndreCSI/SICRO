    "\n",
    "\n",
    "class CalendarioPopup(tk.Toplevel):\n",
    "    \"\"\"Calendario nativo em Tkinter puro, sem dependencias externas.\"\"\"\n",
    "\n",
    "    def __init__(self, parent, entry_destino):\n",
