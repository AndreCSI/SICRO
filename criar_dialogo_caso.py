    "        else: self._mes += 1\n",
    "        self._build()\n",
    "\n",
    "    def _selecionar(self, data):\n",
    "        self._data = data\n",
    "        self.entry.delete(0, tk.END)\n",
    "        self.entry.insert(0, data.strftime('%d/%m/%Y'))\n",
    "        self.destroy()\n",
