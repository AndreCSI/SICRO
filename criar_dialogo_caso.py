    "\n",
    "class DialogoDadosCaso(tk.Toplevel):\n",
    "    \"\"\"Formulario de dados do caso: BO, requisicao, local, municipio, perito, data.\"\"\"\n",
    "\n",
    "    def __init__(self, parent, modo='zero'):\n",
    "        super().__init__(parent)\n",
    "        self.title('Dados do caso')\n",
    "        self.configure(bg=COR_FUNDO)\n",
    "        self.resizable(False, False)\n",
    "        self.grab_set()\n",
    "        self.resultado = None\n",
    "        w, h = 480, 400\n",
    "        sw, sh = self.winfo_screenwidth(), self.winfo_screenheight()\n",
    "        self.geometry(f'{w}x{h}+{(sw-w)//2}+{(sh-h)//2}')\n",
    "        tk.Frame(self, bg=AMARELO, height=4).pack(fill='x')\n",
    "        corpo = tk.Frame(self, bg=COR_FUNDO)\n",
    "        corpo.pack(fill='both', expand=True, padx=24, pady=16)\n",
    "        titulo = 'Croqui do zero' if modo=='zero' else 'Croqui sobre drone'\n",
    "        tk.Label(corpo, text=f'  {titulo}',\n",
    "                 font=FONTE_SUB, bg=COR_FUNDO, fg=AMARELO).pack(anchor='w', pady=(0,12))\n",
    "        campos = [\n",
    "            ('No do BO',           'bo',         ''),\n",
    "            ('No da Requisicao',   'requisicao',  ''),\n",
    "            ('Local (ruas/av.)',   'local',       ''),\n",
    "            ('Municipio',          'municipio',   'Porto Grande'),\n",
    "            ('Perito responsavel', 'perito',      'Andre Ricardo Barroso'),\n",
