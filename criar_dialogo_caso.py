    "    def _build(self):\n",
    "        for w in self.winfo_children():\n",
    "            w.destroy()\n",
    "        tk.Frame(self, bg=AMARELO, height=3).pack(fill='x')\n",
    "        nav = tk.Frame(self, bg=COR_PAINEL)\n",
    "        nav.pack(fill='x', padx=4, pady=4)\n",
    "        tk.Button(nav, text='<', font=('Segoe UI',10), width=2,\n",
    "                  bg=COR_PAINEL, fg=BRANCO, relief='flat', cursor='hand2',\n",
    "                  command=self._mes_anterior).pack(side='left')\n",
    "        tk.Button(nav, text='>', font=('Segoe UI',10), width=2,\n",
    "                  bg=COR_PAINEL, fg=BRANCO, relief='flat', cursor='hand2',\n",
    "                  command=self._mes_seguinte).pack(side='right')\n",
    "        MESES = ['Janeiro','Fevereiro','Marco','Abril','Maio','Junho',\n",
    "                 'Julho','Agosto','Setembro','Outubro','Novembro','Dezembro']\n",
    "        tk.Label(nav, text=f'{MESES[self._mes-1]} {self._ano}',\n",
    "                 font=('Segoe UI',10,'bold'),\n",
